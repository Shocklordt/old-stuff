var choice = prompt("Select A, B, C or D", "");
switch(choice) {
    case "A":
        alert("Selected A");
        break;
    case "B":
        alert("Selected B");
        break;
    case "C":
        alert("Selected C");
        break;
    case "D":
        alert("Selected D");
        break;
    default:
        alert("Wrong selection");
        break;
}